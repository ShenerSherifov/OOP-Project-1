package bg.tu_varna.sit.oop1.enums;

/**
 * Represents the status of a student in the university.
 */
public enum StudentStatus {
    /**
     * Indicates that the student is actively enrolled in courses.
     */
    ENROLLED,

    /**
     * Indicates that the student has interrupted the education program.
     */
    DROPPED,

    /**
     * Indicates that the student has successfully completed the whole program.
     */
    GRADUATED
}
